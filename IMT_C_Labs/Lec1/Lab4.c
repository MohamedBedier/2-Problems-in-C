/* include standard input output library */
#include <stdio.h>


int main()
{
	/* define variables */
	int Number_1 = 7 , Number_2 = 4;
	
	
	printf("Number_1 & Number_1 = %d\n",Number_1&Number_2); 
	printf("Number_1 | Number_1 = %d\n",Number_1|Number_2); 
	printf("Number_1 ^ Number_1 = %d\n",Number_1^Number_2);
	printf("Number_1 >> 1 = %d\n",Number_1>>1);
	printf("Number_2 << 2 = %d\n",Number_2<<2);
}